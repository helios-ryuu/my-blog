-- Helios Blog v2.0 - complete fresh database initialization
-- This is the single source of truth for the project database schema.
begin;

create extension if not exists pgcrypto;

do $$
begin
    create type public.post_level as enum ('beginner', 'intermediate', 'advanced');
exception
    when duplicate_object then null;
end
$$;

create table if not exists public.category (
    id bigint generated by default as identity primary key,
    name text not null unique,
    slug text not null unique,
    icon text,
    description text not null,
    examples text not null default '',
    display_order integer not null default 0,
    created_at timestamptz not null default now(),
    updated_at timestamptz,
    constraint category_name_length check (char_length(trim(name)) between 1 and 80),
    constraint category_slug_format check (slug ~ '^[a-z0-9]+(?:-[a-z0-9]+)*$'),
    constraint category_slug_length check (char_length(slug) <= 100),
    constraint category_icon_length check (icon is null or char_length(trim(icon)) between 1 and 16),
    constraint category_description_length check (char_length(trim(description)) between 1 and 300),
    constraint category_examples_length check (char_length(examples) <= 500),
    constraint category_display_order_positive check (display_order >= 0)
);

